public class prefixpostfix {
    public static void main(String[] args) {
        int prefixValue = 10;
        System.out.println(++prefixValue);
        System.out.println(prefixValue);
        int postfix =20;
        //Note vlaue does not change in the same line
        System.out.println(postfix++);
        System.out.println(postfix);        
    }
    
}
